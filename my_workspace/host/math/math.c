int math_add(int a, int b)
{
	return a + b;
}

int math_sub(int a, int b)
{
	return a - b;
}

long long int math_mult(int a, int b)
{
	return (long long int) a * b;
}

float math_div(int a, int b)
{
	return (float)a / b;
}
